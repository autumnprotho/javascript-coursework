const age = 30;
const fullName = "Autumn Protho";
const isEnrolled = true;

// Undefined variable (declared but not assigned) let futureGoal;
// Null variable (intentional absence of a value) let completedCourse = null;

console.log(`Age: ${age}, Type: ${typeof age}`);
console.log(`Name: ${fullName}, Type: ${typeof fullName}`);
console.log(`Enrolled: ${isEnrolled}, Type: ${typeof isEnrolled}`);
console.log(`Future Goal: ${futureGoal}, Type: ${typeof futureGoal}`);
console.log(`Completed Course: ${completedCourse}, Type: ${typeof completedCourse}`);

/* I learned that undefined means a variable has been declared but has not yet been assigned a value. 
Null is intentionally assigned to represent no value. 
Developers use null when they want to clearly indicate the absence of data. */
